
using System.Collections.Generic;
using System.Linq;
using FluentAssertions;
using Xunit;
using Xunit.Abstractions;
using Yarn.Compiler;
using System.IO;

using YarnSpinner.Diagnostics;

namespace YarnSpinner.Tests
{

    public partial class GeneratedDiagnosticTests : TestBase
    {
        public GeneratedDiagnosticTests(ITestOutputHelper outputHelper) : base(outputHelper)
        {
        }

        [Theory]
        [MemberData(nameof(GeneratedData))]
        public void TestDiagnostic(string code, int exampleIndex)
        {
            var descriptorData = DescriptorData[code];

            var example = descriptorData.Examples[exampleIndex];
            // The YAML frontmatter parser is (for some reason) omitting the
            // '---' from the block strings, so as a bad workaround we're using
            // '-=-' as a placeholder. I hate it!
            var source = example.Script.Replace("-=-", "---");

            var job = CompilationJob.CreateFromString("<input>", source);
            job.Library = new Yarn.Dialogue.StandardLibrary();
            job.Options = new Dictionary<string, string>() { { Compiler.Options.GenerateBlockGraph, "true" } };
            job.Library.RegisterFunction("visited", (string nodeName) => true);

            var result = Compiler.Compile(job);

            var diagnosticDescriptor = DiagnosticDescriptor.GetDescriptor(code);
            diagnosticDescriptor.Should().NotBeNull();

            result.Diagnostics.Should().Contain(d => d.Code == diagnosticDescriptor.Code);
        }

        [Theory]
        [MemberData(nameof(GetCompilerDiagnosticCodes))]
        public void TestCompilerDiagnosticsHaveTestData(string code)
        {
            var diag = DiagnosticDescriptor.GetDescriptor(code);
            code.Should().NotBeNull();
            diag.Should().NotBeNull($"{code} should be a valid diagnostic code");

            var descriptorData = DescriptorData[code];

            if (ShouldProduceTestsForDiagnostic(descriptorData))
            {
                descriptorData.Examples.Should().NotBeEmpty();
            }
        }

        private static IEnumerable<DiagnosticFrontMatter> AllDescriptorData
        {
            get
            {
                var path = Path.Join(ProjectRootPath, "YarnSpinner.Diagnostics/Definitions");
                var allFiles = Directory.EnumerateFiles(path, "*.md");
                return allFiles.Select(path =>
                {
                    var contents = File.ReadAllText(path);
                    var (info, _) = contents.GetFrontMatter<DiagnosticFrontMatter>();
                    return info;
                });
            }
        }

        private static readonly Dictionary<string, DiagnosticFrontMatter> DescriptorData = AllDescriptorData.ToDictionary(kv => kv.Code);

        public static IEnumerable<object[]> GetGeneratedTestData()
        {
            foreach (var descriptorData in AllDescriptorData)
            {
                if (ShouldProduceTestsForDiagnostic(descriptorData) == false)
                {
                    continue;
                }
                int i = 0;
                foreach (var example in descriptorData.Examples)
                {
                    yield return new object[] { descriptorData.Code, i };
                    i++;
                }
            }
        }

        public static IEnumerable<object[]> GetCompilerDiagnosticCodes()
        {
            return DescriptorData.Keys.Select(k => new object[] { k });
        }

        // GetGeneratedTestData() is generated by the source generator
        public static readonly IEnumerable<object[]> GeneratedData = GetGeneratedTestData();



        private static bool ShouldProduceTestsForDiagnostic(DiagnosticFrontMatter diagnostic)
        {
            var descriptorData = diagnostic;
            if (descriptorData.SkipTestGeneration)
            {
                // Diagnostics marked as "we deliberately have no test for this"
                // are excluded
                return false;
            }
            if (descriptorData.GeneratedIn != DiagnosticFrontMatter.Source.Compiler)
            {
                // Diagnostics that are not generated in the compiler (which is
                // what we're testing) are excluded
                return false;
            }
            if (descriptorData.DeprecatedVersion != null)
            {
                // Diagnostics that are deprecated are excluded
                return false;
            }

            return true;
        }
    }
}
