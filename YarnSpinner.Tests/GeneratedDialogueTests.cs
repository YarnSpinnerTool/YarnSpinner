
using System.Collections.Generic;
using System.Linq;
using FluentAssertions;
using Xunit;
using Xunit.Abstractions;
using Yarn.Compiler;
namespace YarnSpinner.Tests
{

    public partial class GeneratedDiagnosticTests : TestBase
    {
        public GeneratedDiagnosticTests(ITestOutputHelper outputHelper) : base(outputHelper)
        {
        }

        [Theory]
        [MemberData(nameof(GeneratedData))]
        public void TestDiagnostic(string code, string source)
        {
            var job = CompilationJob.CreateFromString("<input>", source);
            job.Library = new Yarn.Dialogue.StandardLibrary();
            job.Library.RegisterFunction("visited", (string nodeName) => true);

            var result = Compiler.Compile(job);

            var diagnosticDescriptor = DiagnosticDescriptor.GetDescriptor(code);
            diagnosticDescriptor.Should().NotBeNull();

            result.Diagnostics.Should().Contain(d => d.Code == diagnosticDescriptor.Code);
        }

        // GetGeneratedTestData() is generated by the source generator
        public static readonly IEnumerable<object[]> GeneratedData = GetGeneratedTestData();

        [Theory]
        [MemberData(nameof(GetCompilerDiagnosticCodes))]
        public void TestCompilerDiagnosticsHaveTestData(string code)
        {
            var diag = DiagnosticDescriptor.GetDescriptor(code);
            code.Should().NotBeNull();

            GeneratedData.Should().Contain(item => (string)item[0] == code, $"a test case should exist for {code} ('{diag.Description}')");
        }
    }
}
